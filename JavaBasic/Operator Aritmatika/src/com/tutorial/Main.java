package com.tutorial;

public class Main {
    public static void main(String[] args) {
        // operator aritmatika

        int variabel1 = 50;
        int variabel2 = 10;
        int hasil;

        // 1. penjumlahan
        hasil = variabel1 + variabel2;
        System.out.println(variabel1 + "+" + variabel2 + "=" + hasil);
        // 2. pengurangan 1
        hasil = variabel1 - variabel2;
        System.out.println(variabel1 + "-" + variabel2 + "=" + hasil);
        // 2. pengurangan 2
        hasil = variabel1 - variabel2;
        System.out.printf("%d-%d = %d \n", variabel1, variabel2, hasil);
        // 3. perkalian
        hasil = variabel1*variabel2;
        System.out.printf("%d*%d = %d \n", variabel1, variabel2, hasil);
        // 3. perkaian 2
        hasil = variabel1*variabel2;
        System.out.println(variabel1 + "*" + variabel2 + "=" + hasil);
        // 4. pembagian
        hasil = variabel1/variabel2;
        System.out.println(variabel1 + "/" + variabel2 + "=" + hasil);

        float a = 6;
        float b = 5;
        float hasilFloat = a/b;
        System.out.println(a + "/" + b + "=" + hasilFloat);
        // 5. modulus
        hasil = variabel1%variabel2;
        System.out.printf("%d %% %d = %d \n", variabel1, variabel2, hasil);

        int v = 29;
        int e = 10;
        int hasil2;
        hasil2 = v%e;
        System.out.printf("%d %% %d = %d \n", v = 29, e = 10, hasil2);




    }
}
